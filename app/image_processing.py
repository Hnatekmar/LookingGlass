async def prepare_image_for_deepseek_ocr(upload_file: bytes) -> Image.Image:
    """
    Prepare an image for DeepSeek-OCR vLLM inference.
    Resizes image to maximum 1024x1024 while keeping aspect ratio.
    """
    # Read the uploaded file
    image_data = io.BytesIO(upload_file)

    # Open and convert to RGB
    img = Image.open(image_data)

    # Convert to RGB if necessary
    if img.mode != 'RGB':
        if img.mode in ('RGBA', 'LA', 'P'):
            # Create white background for transparency
            background = Image.new('RGB', img.size, (255, 255, 255))
            if img.mode == 'P':
                img = img.convert('RGBA')
            if img.mode == 'RGBA':
                background.paste(img, mask=img.split()[-1])
            else:
                background.paste(img)
            img = background
        else:
            img = img.convert('RGB')

    # Resize image to maximum 1024x1024 while preserving aspect ratio
    max_size = 512
    img.thumbnail((max_size, max_size), Image.Resampling.LANCZOS)

    # Convert to grayscale
    img = img.convert("L")

    # Return PIL Image directly - no resizing
    output = io.BytesIO()
    img.save(output, format='JPEG', quality=90)
    # Return the bytes
    return output.getvalue()

